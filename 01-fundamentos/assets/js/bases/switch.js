
// La estructura Switch me permite realizar multiples evaluaciones de valores exactos
// Se usa mucho en nmenus de selecci√≥n

let dia = 0;


switch ( dia ){
    case 0:
        console.log('Domingo');
        break;
    case 1:
        console.log('Lunes');
        break;
    case 2:
        console.log('Martes');
        break;
    case 3:
        console.log('Miercoles');
        break;
    case 4:
        console.log('Jueves');
        break;
    case 5:
        console.log('Viernes');
        break;
    case 6:
        console.log('Sabado');
        break;
    default:
        console.log('Dia no valido');

}
